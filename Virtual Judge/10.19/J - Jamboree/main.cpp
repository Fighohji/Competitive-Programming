void solve() {
    int n, m;
    cin >> n >> m;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }
    sort(all(a));
    if (m >= n) {
        cout << a[n - 1] << '\n';
    } else {
        int odd = m * 2 - n;
        int Ans = 0, i;
        for (i = n - 1; i >= 0 && odd > 0; --i, --odd) {
            Ans = max(Ans, a[i]);
        }
        int j = 0;
        while (j < i) {
            Ans = max(Ans, a[j] + a[i]);
            j++, i--;
        }
        cout << Ans << '\n';
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int _ = 1;
    while (_--)
        solve();
    return 0;
}
