void solve() {
    int n;
    cin >> n;
    vector G(n + 1, vector<int>());
    vector<int> deg(n + 1);
    for (int i = 1; i < n; ++i) {
        int u, v;
        cin >> u >> v;
        G[u].push_back(v);
        G[v].push_back(u);
        deg[v]++, deg[u]++;
    }
    int cnt = 0;
    for (int i = 1; i <= n; ++i) {
        cnt += deg[i] == 1;
    }
    if (n == 2) {
        cout << "Bob\n";
        return;
    }
    if ((n - 1 - cnt) % 2) {
        cout << "Alice\n";
    } else {
        cout << "Bob\n";
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int _ = 1;
    while (_--)
        solve();
    return 0;
}
